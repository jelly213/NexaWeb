/** Resolve a dot path like "chapters.2.headline" to its {fr,en} leaf, or null. */
export function leaf(path) {
  let node = copy;
  for (const part of path.split('.')) {
    if (node === null || typeof node !== 'object') return null;
    node = node[part];
  }
  if (node && typeof node === 'object' && 'fr' in node) return node;
  return null;
}
